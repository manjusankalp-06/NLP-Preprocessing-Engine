import torch
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import warnings
warnings.filterwarnings('ignore')

from datasets import load_dataset
from transformers import (
    AutoTokenizer,
    AutoModelForSequenceClassification,
    TrainingArguments,
    Trainer
)
from sklearn.metrics import (
    accuracy_score,
    precision_score,
    recall_score,
    f1_score,
    confusion_matrix,
    ConfusionMatrixDisplay,
    classification_report
)

print("All imports done")
print("GPU:", torch.cuda.is_available())
print("Device:", torch.cuda.get_device_name(0)
      if torch.cuda.is_available() else "CPU")

dataset = load_dataset("imdb")
print(dataset)
print("\nSample:")
print(dataset["train"][0]["text"][:300])
print("Label:", dataset["train"][0]["label"])

train_dataset = dataset["train"]\
    .shuffle(seed=42).select(range(2000))

val_dataset = dataset["test"]\
    .shuffle(seed=42).select(range(500))

test_dataset = dataset["test"]\
    .shuffle(seed=42).select(range(500, 1000))

print("Train size:", len(train_dataset))
print("Val size  :", len(val_dataset))
print("Test size :", len(test_dataset))

# Check distribution
print("\nTrain Labels:")
labels = train_dataset["label"]
print("Positive:", labels.count(1))
print("Negative:", labels.count(0))

tokenizer = AutoTokenizer.from_pretrained(
    "bert-base-uncased"
)
print("Tokenizer loaded")

def tokenize_function(examples):
    return tokenizer(
        examples["text"],
        padding="max_length",
        truncation=True,
        max_length=128
    )

train_dataset = train_dataset.map(
    tokenize_function, batched=True)
val_dataset = val_dataset.map(
    tokenize_function, batched=True)
test_dataset = test_dataset.map(
    tokenize_function, batched=True)

print("Tokenization done")

cols = ["input_ids", "attention_mask", "label"]

train_dataset.set_format(
    type="torch", columns=cols)
val_dataset.set_format(
    type="torch", columns=cols)
test_dataset.set_format(
    type="torch", columns=cols)

print("Format set successfully")
print("Sample keys:",
      list(train_dataset[0].keys()))

model = AutoModelForSequenceClassification\
    .from_pretrained(
        "bert-base-uncased",
        num_labels=2
    )
print("Model loaded")
print("Parameters:",
      sum(p.numel()
          for p in model.parameters()))

def compute_metrics(pred):
    labels = pred.label_ids
    preds = pred.predictions.argmax(-1)

    acc  = accuracy_score(labels, preds)
    prec = precision_score(labels, preds)
    rec  = recall_score(labels, preds)
    f1   = f1_score(labels, preds)

    return {
        "accuracy" : round(acc,  4),
        "precision": round(prec, 4),
        "recall"   : round(rec,  4),
        "f1"       : round(f1,   4)
    }

print("compute_metrics defined")

training_args = TrainingArguments(
    output_dir="./results",
    eval_strategy="epoch",
    save_strategy="epoch",
    learning_rate=2e-5,
    per_device_train_batch_size=16,
    per_device_eval_batch_size=16,
    num_train_epochs=2,
    weight_decay=0.01,
    fp16=True,
    logging_steps=100,
    load_best_model_at_end=True,
    report_to="none"
)
print("Training args defined")

trainer = Trainer(
    model=model,
    args=training_args,
    train_dataset=train_dataset,
    eval_dataset=val_dataset,
    compute_metrics=compute_metrics
)
print("Trainer ready")

print("Training started...")
trainer.train()

print("Evaluating...")
results = trainer.evaluate(test_dataset)

print("\n===== TEST RESULTS =====")
for key, val in results.items():
    print(f"{key}: {round(val, 4)}")

predictions = trainer.predict(test_dataset)

y_pred = np.argmax(
    predictions.predictions, axis=1)
y_true = predictions.label_ids

print("Unique predictions:", np.unique(y_pred))
print("Unique true labels:", np.unique(y_true))
print("Sample preds:", y_pred[:10])
print("Sample true :", y_true[:10])

print("=" * 40)
print("BERT FINE-TUNING RESULTS")
print("=" * 40)
print("Accuracy :",
      round(accuracy_score(
          y_true, y_pred), 4))
print("Precision:",
      round(precision_score(
          y_true, y_pred), 4))
print("Recall   :",
      round(recall_score(
          y_true, y_pred), 4))
print("F1 Score :",
      round(f1_score(
          y_true, y_pred), 4))
print("=" * 40)

cm = confusion_matrix(y_true, y_pred)

plt.figure(figsize=(6, 5))
sns.heatmap(
    cm,
    annot=True,
    fmt='d',
    cmap='Blues',
    xticklabels=["Negative", "Positive"],
    yticklabels=["Negative", "Positive"]
)
plt.xlabel("Predicted Label")
plt.ylabel("True Label")
plt.title("Confusion Matrix - BERT")
plt.tight_layout()
plt.show()

